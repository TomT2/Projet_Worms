            // TODO: Add your update logic here
            backgroundmanagement.Update();

            foreach (Entity entity in entities)
            {
                entity.Update(gameTime);

                entityPhysic.Gravity(entity);
                entity.IsGrounded = false;

                foreach (Tiles tile in map.Tiles)
                    if (tile.Name != "c4")
                    {
                        entityPhysic.AnticipationColisions(entity, tile.Rectangle);
                        if (entityPhysic.Colision(entity.Hitbox, entity.Velocity, tile.Rectangle) && entity.Type == "Missile")
                        {
                            entity.IsMyTurn = false;
                        }
                    }
            }

            base.Update(gameTime);
        }

